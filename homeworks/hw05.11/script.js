
var age = prompt("Өз жасыңызды енгізіңіз:");

if (age >= 18) {
  alert("  Сіз ересексіз!  ");
} 
else {
  alert("  Сіз кәмелетке толмағансыз!  " );
}

var temperature = prompt("Сырттағы температураны енгізіңіз:");


if (temperature >= 25) {
  alert(" Шорты мен футболка киіңіз.! ");
} 
else {
  alert(" Қалың куртка, жемпір киіңіз.! ");
}

